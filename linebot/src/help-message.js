'use strict';

const HELP_MESSAGE_MAX_LENGTH = 1500;

function splitHelpText(text, maxLength = HELP_MESSAGE_MAX_LENGTH) {
  const sections = String(text || '')
    .split('\n\n')
    .map(section => section.trim())
    .filter(Boolean);
  const chunks = [];
  let current = '';

  for (const section of sections) {
    const next = current ? `${current}\n\n${section}` : section;
    if (next.length <= maxLength) {
      current = next;
      continue;
    }

    if (current) {
      chunks.push(current);
      current = '';
    }

    if (section.length <= maxLength) {
      current = section;
      continue;
    }

    const lines = section.split('\n');
    let lineChunk = '';
    for (const line of lines) {
      const lineNext = lineChunk ? `${lineChunk}\n${line}` : line;
      if (lineNext.length <= maxLength) {
        lineChunk = lineNext;
      } else {
        if (lineChunk) chunks.push(lineChunk);
        lineChunk = line;
      }
    }
    if (lineChunk) {
      current = lineChunk;
    }
  }

  if (current) chunks.push(current);
  return chunks;
}

